import {HOSPITALS, PATHOLOGIES, EM_SERVICE, PART_SERVICE} from '../utils/urls';

const getHospitals = () => {
  fetch(HOSPITALS, {
    method: 'GET',
    headers: {
      Accept: 'application/json',
      'Content-Type': 'application/json',
    },
  })
    .then(response => response.json())
    .then(json => {
      return json;
    })
    .catch(error => {
      console.error(error);
    });
};

